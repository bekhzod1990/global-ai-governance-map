export const COUNTRIES: Country[] = COUNTRY_SEED.map((seed) => ({
  iso3: seed.iso3,
  name: seed.name,
  region: seed.region,
  isEUMember: EU_MEMBER_ISO3.includes(seed.iso3),
  nationalAIRegulationIds: [],
  internationalParticipationIds: [],
  notes: seed.notes,
}));

export const COUNTRY_BY_ISO3: Record<string, Country> = COUNTRIES.reduce(
  (acc, c) => {
    acc[c.iso3] = c;
    return acc;
  },
  {} as Record<string, Country>
);

export function getCountryName(iso3: string): string {
  return COUNTRY_BY_ISO3[iso3]?.name ?? iso3;
}

